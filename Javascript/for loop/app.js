// The for loop is very easy in JavaScript

//There are three types of variable declaration in JS: var, let, and const

//for loops commonly use var type of variables.

for (
  var i = 1; // Here we used i variable of type var
  i <= 10; // with the initial value of 1 and ending value of 10.
  i++ //and here we are incrementing +1 on every loop completion.
) {
  console.log(i);
  //Console here is used for printing our desired value or sometimes checking function's output.
}
